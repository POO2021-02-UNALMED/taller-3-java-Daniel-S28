package taller3.televisores;

public class Marca {
	private String nombre;
	
	public Marca (String nombre) {
		this.nombre=nombre;
	}
	
	public String getMarca() {
		return nombre;
	}
	public void setMarca(String nom) {
		nombre=nom;
	}
	
	public String getnombre() {
		return nombre;
	}
	public void setnombre(String nom) {
		nombre=nom;
	}
}
